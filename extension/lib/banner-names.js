const DATE_PREFIX = /^\s*\d{4}-\d{2}-\d{2}\s*~\s*\d{4}-\d{2}-\d{2}\s*:\s*/;
const DATE_RANGE = /(\d{4}-\d{2}-\d{2})\s*~\s*(\d{4}-\d{2}-\d{2})/;

// 由 godfat option 文字取出日期區間字串「YYYY-MM-DD~YYYY-MM-DD」；無則回空字串
export function bannerDateRange(text) {
  const m = (text || '').match(DATE_RANGE);
  return m ? `${m[1]}~${m[2]}` : '';
}

// 由 godfat 名稱（可含日期前綴）推導簡稱
export function shortBannerName(text) {
  const name = (text || '').replace(DATE_PREFIX, '');
  for (const [re, short] of RULES) if (re.test(name)) return short;
  return fallback(name);
}

function fallback(name) {
  let s = name.replace(/[★☆].*$/su, ''); // 去掉「★點圖確認詳細吧!!」等罐頭字
  s = s.replace(/[！!。，、\s]+$/u, '').trim();
  if (s.length > 12) s = s.slice(0, 12) + '…';
  return s || name;
}
